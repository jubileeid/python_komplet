# Konversi tipe data dari integer menjadi string
x = 10
x_str = str(x)
print("Nilai x_str:", x_str)
print("Tipe data x_str:", type(x_str))

# Konversi tipe data dari string menjadi integer
y_str = "20"
y = int(y_str)
print("Nilai y:", y)
print("Tipe data y:", type(y))

# Konversi tipe data dari string menjadi float
z_str = "3.14"
z = float(z_str)
print("Nilai z:", z)
print("Tipe data z:", type(z))
